package basics.ch_03_variables;

public class VariableDemo2 {

	public static void main(String[] args) {
		// Declaring the string variable
		// String variable will have "null" value if not initialized
		
		String subject;   // Declaration 
		// System.out.println(subject); // throw error
		
		// Initialize the string variable
		subject = "java"; // Initialization 
		System.out.println(subject);
		
		// Declaring a int variable
		// int variable will have a 0 value if not initialized
		int number; // Declaration
		// System.out.println(number); // throw error
		
		number = 100; // Initialization
		System.out.println(number);
	}
	
}
